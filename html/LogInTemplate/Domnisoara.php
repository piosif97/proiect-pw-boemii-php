<b>Regia:</b> Alexandru Maftei<br>
<b>Cu:</b> Maia Morgenstern, Ioana Anastasia Anton, Ioana Sandu, Tudor Aaron Istodor<br>
<i>Inspirat din Domnişoara Christina (Mircea Eliade)</i><br><br>



										<span class="image featured"><img src="images/DC.jpg" alt="" /></span>
										<p>Domnişoara Christina este cel mai recent film al regizorului român Alexandru Maftei, prezentat în premieră absolută ieri, în prima zi dedicată filmului autohton la TIFF. Un film într-adevăr atât de recent, încât, la o primă vedere, nici măcar câteva fotografii care să ofere indicii cât de mărunte nu se găsesc. Un film care a umplut cinematograful Florin Piersic (ex-Republica) din centrul Clujului, cinematograf care, e lucru cunoscut, dispune de peste 800 de locuri.</p>

										<video width="800" height="600" controls>
  										<source src="filme/domnisoara-christina.mp4" type="video/mp4">
										</video> <br><br>

<p>Lungmetrajul este inspirat din opera cu acelaşi nume, aparţinând lui Mircea Eliade. Pentru că nu am citit-o (deşi ieri îmi aminteam, uşor nostalgică, cum am ţinut un mic discurs pe tema prozei fantastice a lui Eliade – subiectul al treilea la proba orală de bac!), nu mă pot pronunţa în ce măsură transpunerea pe marele ecran i-ar fi sau nu fidelă. Nici nu cred că are importanţă. Am avut ieri ocazia de a viziona un film închegat, cu substanţă, beneficiind de participarea a trei generaţii de actori, fiecare aducându-şi aportul şi cinstindu-şi arta cu o ofrandă pe măsură.Astfel, producţia filmată în împrejurimile Bacăului (conform declaraţiei de ieri a regizorului, prezent la premieră, alături de un număr impresionant de membri ai echipei de producţie şi de actori) reuneşte maturitatea şi experienţa Maiei Morgenstern (doamna Moscu) sau a lui Ovidiu Ghiniţă (profesorul Nazarie), exuberanţa Ioanei Anastasia Anton (Sanda) şi a lui Tudor Aaron Istodor (Egor Paşchievici), şi – fireşte – impresionanta performanţă a actriţei-copilă Ioana Sandu (Simina).</p>
<p>Firul narativ este simplu, uşor de urmărit, însă prezentat într-un dans alegoric de lumini şi umbre care dă naştere unei sumedenii de întrebări şi speculaţii. Sanda, o domnişoară plină de viaţă şi de farmecul secolului, îl aduce pe Egor, un tânăr pictor, la moşia familiei, pentru a-l prezenta mamei sale. La conac se mai află şi profesorul Nazarie, preocupat de săpăturile arheologice de la Bălănoaia. Tabloul este completat de Simina, surioara Sandei, o prezenţă care se remarcă de la bun început prin gesturi şi mimică; o prezenţă care, treptat, devine tot mai bizară, sfârşind prin a speria de-a binelea cu apucăturile de „vrăjitoare mică” şi poveştile întunecate.</p>
<p>
Cuprinşi de senzaţii neplăcute, simţindu-se nelalocul lor în casă, pe timp de noapte, Egor şi profesorul Nazarie îşi ţin companie. Discuţiile lor, îmbinate cu remarcile fugitive ale membrelor familiei, scot la iveală o legendă cruntă: povestea domnişoarei Christina, sora mai mare a doamnei Moscu, ucisă la doar 20 de ani în timpul răscoalelor din 1907. Şi pentru că ambele surori Moscu – atât Sanda, cât şi Simina – susţin că mătuşa lor defunctă le-ar vizita mereu în vis, Egor îşi manifestă curiozitatea şi dezvăluie o ţesătură putredă şi înşelătoare de jocuri perfide; o lume interioară, decadentă, în care nimic nu este ce pare a fi. Totul începe cu un portret, executat cu atâta măiestrie, de parcă modelul însuşi s-ar fi aşezat cuminte în ramă, umplând spaţiul cu prezenţa sa, de o frumuseţe rece, misterioasă şi crudă.
</p>
<p>
Complimente scenografilor şi costumierilor: rezultatele sunt de excepţie. Complimente actorilor, care au intrat foarte bine în pielea personajelor şi le-au conferit autenticitatea de care este absolută nevoie pentru a da credibilitate unei fresci care zugrăveşte o altă eră. Din nou – Ioana Sandu. Această fetiţă, de o timiditate excesivă faţă în faţă cu publicul, a dovedit un talent actoricesc şi o siguranţă de sine dincolo de limite, faţă în faţă cu camera. Şi, fiindcă pomenesc din nou actorii, nu pot lăsa neobservată prezenţa în aceleaşi cadre a unei familii: Maia Morgenstern şi Tudor Aaron Istodor, mamă şi fiu.

Dacă vi se oferă ocazia, mergeţi să vedeţi Domnişoara Christina. Fără să exagerez, bănuiesc că ar avea un succes nebun în cinematografele americane; în fond, însumează fastul şi decadenţa unei epoci apuse, credinţele şi superstiţiile din popor, tragedia unor tineri prinşi într-un joc al supranaturalului aflat mai presus de înţelegerea lor – totul în imagini şi culori sublime. În plin boom al filmelor cu vampiri, vârcolaci şi creaturi supranaturale, Domnişoara Christina s-ar înscrie ca o capodoperă a genului, cu o viziune mult superioară unei întregi armate de filme pentru publicul pre-adolescent.
</p>


										
													
