										<p><i>„În secolul al optsprezecelea a trăit în Franța unul dintre cei mai geniali și mai detestabili bărbați dintr-un 
											veac nu sărac în personaje geniale și detestabile.”</i></p>
											<p>Povestea lui Jean-Baptiste Grenouille, începe cu nașterea sa la o tarabă de pește din Rue aux Fers, unde mama 
											acestuia speră să aibă același final ca ceilalți avortoni care l-au precedat. Dar soarta a avut alte planuri și l-a 													cruțat, asigurându-i un traseu anevoios în viață – mai întâi la parohia părintelui Terrier, apoi la pensiunea 
											pentru copii a madamei Gaillard și în cele din urmă ca ucenic la tabăcăria lui Grimal, apoi la parfumeria
											 Baldini în Paris și în cele din urmă la parfumeria din Grasse.</p>

										<p>Cartea este construită asemeni unui parfum, cu notele ușoare, de vârf însumate de copilăria lui Grenouille, 
										apoi notele de mijloc în mijlocul cărora se ascunde adolescența și în care descoperă parfumul care-i va marca viața, 
										ca în cele din urmă să rămână notele de bază, cu toată întunecimea lor, îmbibată cu izul crimelor comise de acesta 
										și al terorii răspândite să prezinte tinerețea protagonistului.</p>
										<p>Suskind creează o lume de o bogăție olfactivă inimaginabilă, pe care o redă magistral prin scenele vii, decupate 
										deopotrivă din iadul olfactiv al Parisului și din paradisul promis al parfumierilor cu mireasma dulceagă și îmbătătoare 
										a narciselor, zambilelelor, iasomiei, trandafirilor, lavandei și tuberozelelor din Grasse.</p>

										<p>Mijlocul și baza unui parfum reprezintă de fapt tema lui, definindu-l, lucru de care se folosește și autorul în construcția 												romanului și a personajului principal care se învârt în jurul mirosului. Oropsindu-l pe Grenouille cu defecte fizice, cu boli, 
										cu vicisitudinile vieții dar mai ales cu absența unui miros propriu, Suskind se asigură că îi oferă acestuia toate condițiile 										necesare pentru a ridica la rang de obsesie pasiunea pentru miresme, izuri, arome, dotându-și totodată personajul cu un simț al 										mirosului supradezvoltat, care umbrește toate celelalte simțuri și însuși rațiunea. Singurul mod prin care-și motivează Jean-										Baptiste existența este crearea parfumului suprem, care îi va oferi puterea de a-și inversa rolul jucat, transformându-l dintr-un 																om din fața căruia ceilalți fug și care inspiră milă în unul adorat și adulat.
												</p>
