										în mintea și inima mea. Romanul este unul realist, care surprinde viața cu toate neajunsurile ei, cu bune și cu rele, are o acțiune care 
										curge lent, treptat și se axează pe toate momentele importante din viața eroinei, iar datorită vocii ei puternice și sincere, capabile 
