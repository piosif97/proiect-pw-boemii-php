										Într-un cadru feeric, la malul mării, el îi apare ca un prinț dintr-o poveste, felul lui enigmatic și melancolic o intrigă iar 
										tristețea lui o atrage, o face să își dorească ca ea să fie cea care îi aduce culoare și fericire în existența ce pare cu tente de gri.</p>

										<p>Însă realitatea ei a continuat să fie la fel ca marea pe care o iubește atât de mult: uneori liniștită, dar adesea zbuciumată, răvășitoare, 
										iar vântul necruțător a purtat-o departe de dorințele ei, mereu în situații neprevăzute și alături de persoane care și-au pus amprenta în 
										sufletul ei și au colaborat la zbuciumul ei din interior care nu i-a dat răgaz vreo clipă.</p>
				
										<p>Nici nu mai țin minte de cât timp mi-am dorit să citesc cartea aceasta, uneori am senzația că dintotdeauna, dar mai ales de când am 
