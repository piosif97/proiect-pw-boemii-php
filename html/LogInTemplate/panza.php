										ea nu acceptă ca și accesorii și nu se ghidează după visele și speranțele pe care le făurim, nu avem mereu 
										parte de povestea de dragoste pe care o vrem iar viața nu e un roman pe care îl scrii de dinainte iar apoi 
										te transpui în el și îl trăiești. Important e să mergi mai departe demn indiferent de piedicile care ți se pun 
										în calea fericirii și să înțelegi ceva din toate lecțiile pe care destinul ți le oferă ca un profesor când mai 
										strict, când mai înțelegător.</p>

										<p>Încă de la începutul romanului ”Pânza de păianjen” aflăm de prietenia dintre două tinere, dar pe care diferitele 
										circumstanțe și drumurile separate le-au îndepărtat fizic, dar nu și sufletește. După mulți ani, prenumele Diana, 
										al prietenei ei, apare din nou în viața Ilincăi, iar aceasta pare a fi acum un personaj la fel captivant, dar scandalos, 
										dar care se află pe numele tuturor. Ilinca e intrigată să descopere traseul trecutului prietenei ei și întâmplările care 
										au purtat-o până într-un anumit punct mai critic din prezent. Ea, ca și noi, își va găsi toate răspunsurile în paginile 
										caietelor Dianei, scrise sub forma unor jurnale care au urmărit îndeaproape viața ei începând cu copilăria și până acum.</p>

										<p>Diana Slavu, un copil sărac și obișnuit cu greutățile încă de mică, nu a fost privată de ele nici în adolescență, dar 
