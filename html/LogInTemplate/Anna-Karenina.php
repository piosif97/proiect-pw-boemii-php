											<h2><b>Anna Karenina</b> <text style="color: #f49387;"><i>de Lev Tolstoi </i></text></h2>
										</header>
										<span class="image featured"><img src="images/annak1.jpg" alt="" /></span>
										<h3><i>"Toate familiile  fericite seamănă una cu alta, fiecare familie nefericită este nefericită în felul ei."</i></h3>

										<p>În acest roman se prezintă viața a câtorva familii de nobili, acțiunea se petrece în secolul al XIX-lea, două repere 
										spațiale importante ar fi Moscova și Petersburg-ul. Temele importante ale acestui roman ar fi : iubirea, viața sociala, 
										viața aristocratică, religia.</p>

										<p>Anna Karenina se întoarce cu trenul la Moscova, pentru că fratele ei Stepan Arkadici (Stiva) și-a înșelat soția cu o 
										guvernantă iar Anna trebuia s-o convingă pe cumnata ei, să-i dea o șansă soțului infidel. Anna Karenina era o doamnă respectată, 
										fermecătoare, era soția unui om cunoscut, Alecsei Alxandrovici, aveau și un copil împreună pe nume Serioja. Când se dă jos din tren 
										Anna nu se întâlnește doar cu fratele ei, îl zărește pe Vronski, de care se îndrăgostește. Nu știa ce este dragostea adevărată, se 
										căsătorise cu un bărbat mai mare decât ea cu douăzeci de ani, dar nu din dragoste.</p>

										<p>Levin este unul din prietenii lui Stiva, era îndrăgostit de prințesa Ekaterina Șcerbațkaia (Kitty), se întoarse la Moscova pentru că 
										dorea s-o ceară în căsătorie. Levin află că Vronski îi face de un timp curte lui Kitty, iar când Levin o cere în căsătorie ea îl refuză, 
										părinții ei îl doreau ca ginere pe Vronski. Distrus, Levin pleacă la țară unde stă multă vreme.La bal Kitty observă cât de rece este Vronski 
										și că acesta a preferat să danseze cu Anna Karenina. Când Anna se întoarce acasă Petersburg, Vronski se urcă și el în tren, iar în timpul unei 
										opriri Anna îl vede, iar acesta își exprimă sentimentele pe care le are pentru ea. După un timp Anna cedează sentimentelor, îl iubea pe Vronski, 
										era dezgustată de soț pentru că nu avea niciodată timp pentru ea, Vronski și Anna încep să aibă o aventură, fiind conștienți de consecințe. 
										Toată lumea știa de aventura celor doi care dura de multă vreme, chiar și soțul bănuia, dar lucrurile se complică atunci când ea rămâne însărcinată 
										cu Vronski și vrea să întemeieze o familie cu amantul, soțul se gândește la divorț și să-i ia copilul, pe Serioja.</p>

										<p>Levin se dedicase muncii de tară, iubea pământul, agricultura și dorea să schimbe Rusia, este șocat când află că persoana de care e îndrăgostit 
										(Kitty) este bolnavă și nu s-a măritat în tot acest timp. Levin se adâncește în gânduri de ajunge să se gândească la moarte.</p>
				
										<p>După ce Anna naște copilul și se îmbolnăvește, primește încă o șansă de la soțul ei, dar când ea se însănătoșește pleacă cu Vronski în străinătate, pentru trei luni, părăsindu-și copilul. </p>
 				
										<p>Levin se căsătorește cu Kitty, dar întâmpină greutăți, iar Anna când se întoarce la Moscova este distrusă de faptul că nu poate ieși în societate cu Vronski, de respingerea familiei lui, de 
										respingerea soțului, pentru că nu o lasă să-și vadă copilul. Copilului i se spune că mama lui a murit. Anna începe să fie dependentă de heroină, conștientizând ce a făcut, cum viața ei s-a 
										schimbat radical.</p>

										<p>Romanul scris de Lev Tolstoi surprinde fiecare trăire a personajelor principale și se bucură de   o anumită sensibilitate. Cele două povești de dragoste care se desfășoară în paralel 
										dau o intensitate și o splendoare romanului. Viața din Rusia prezentată de Tolstoi este una complexă, desfășurată în opt părți, care apoi sunt structurate pe capitole. O simetrie șocantă 
										ar fi între Partea I si Partea a VIII-a, două întâmplări tragice care au loc la gară. Soarta unuia din personaje a fost justificată, a plătit pentru tot ce a făcut, într-un mod tragic și 
										impactul acestuia asupra celorlalte personaje este vizibil. Un roman imprevizibil, complex, din care se desprind multe mesaje.</p>
