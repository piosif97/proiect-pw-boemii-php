									<p>Fenomenul a fost observat încă din secolul al III-lea î.Hr., atunci când Aristotel a constatat faptul că, dacă se realizează o gaură într-o cutie, pe peretele opus acesteia se va forma o imagine reală, însă răsturnată.</p>
									<p>Astfel, prima formă de aparat de fotografiat a fost, de fapt, o cameră obscură în care se putea observa imaginea răsturnată a unui obiect aflat în faţa acesteia.</p>	
									<p>În aceeaşi perioadă s-a constatat că lumina înnegreşte clorura de argint şi că hiposulfitul de sodiu poate acţiona precum un „fixator”. Joseph Nicephore Niepce a utilizat aceste trei proceduri pentru a transpune o imagine pe o placă metalică, calitatea fiind relativ ridicată.</p>
