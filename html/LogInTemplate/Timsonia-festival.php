										<p>Facultatea de Muzica si Teatru din cadrul Universitatii de Vest Timisoara, in parteneriat cu Filarmonica Banatul si Asociatia Pro Philharmonia va invita sa participati la prima editie a Festivalului international de muzica contemporana Timsonia.
In perioada 25-28 octombrie 2014, publicul timisorean se va putea bucura de muzica clasica contemporana oferita de tinerii compozitori din tara si din strainatate. Aflat in primul an, acest festival s-a nascut din dorinta de a pune in evidenta deosebita forta creatoare a tinerei generatii care activeaza in domeniul muzicii clasice. Timsonia aduna laolalta muzicieni foarte talentati din centrele culturale de referinta ale tarii. Publicul timisorean va avea posibilitatea de a auzi pentru intaia oara creatii semnate de tineri creatori din Bucuresti, Cluj-Napoca, Iasi sau Timisoara, dar si din Ungaria, Austria, Germania, Italia, Isreal, Coreea de Sud sau Statele Unite ale Americii. Intrarea este libera la toate concertele si conferintele.
In cadrul celor cinci concerte ale festivalului vor fi prezentate noua prime auditii absolute. Lucrarile sunt dedicate festivalului si sunt interpretate de percutionistul Doru Roman, pianistul Mihai Maniceanu, acordeonistul italian Luca Piovesan, ansambluri precum Duo Carillon din Germania, Impact XXI din Cluj-Napoca, Trio Contraste, Percutissimo, Trio Act si Atem din Timisoara.
Tot in cadrul festivalului Timsonia, la Facultatea de Muzica si Teatru vor avea loc patru conferinte deschise publicului. In cadrul acestora se vor dezbate subiecte precum potentialul muzicii electronice in contextul dezvoltarii ulterioare a fenomenului muzical, modalitati noi de abordare a unui instrument cu resurse nebanuite - acordeonul, instrumente noi ce patrund in domeniul muzicii culte, subtilitati ale procesului creator in cadrul muzicii de film.
